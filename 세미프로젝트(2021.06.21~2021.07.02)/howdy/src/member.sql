CREATE TABLE MEMBER(
    MEM_SEQ NUMBER(5) NOT NULL,
    ID VARCHAR2(50) PRIMARY KEY,
    PWD VARCHAR2(50) NOT NULL,
    NAME VARCHAR2(50),
    BIRTH VARCHAR2(50),
    GENDER VARCHAR2(20),
    PHONE VARCHAR2(50),
    EMAIL VARCHAR2(50),
    CATEGORY VARCHAR2(100),
    LOCATION VARCHAR2(100) NOT NULL,
    AUTH NUMBER(1) NOT NULL,	-- 0일반회원, 1관리자
    GROUP_AUTH NUMBER(1) NOT NULL, --0가입대기회원, 1모임원, 2부모임장, 3모임장
    JOINDATE DATE NOT NULL,
    DEL NUMBER(1)
);

